export function teamLogoPath(teamId) {
  return `./assets/img/logos/${teamId}.png`;
}
export function playerImagePath(teamId, playerSlug) {
  return `./assets/img/players/${teamId}/${playerSlug}.png`;
}
export function teamStatPath(teamId, statKey) {
  return `./assets/img/team-stats/${teamId}/${statKey}.png`;
}
